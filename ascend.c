#include<stdio.h>
#include<conio.h>
void main()
{
    int n,i,j,c,a[100];
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=0;i<n;i++)
    {
        for(j=i+1;j<n;j++)
        {
            if(a[i]<a[j])
            {
                c++;
            }
        }
    }
    if(c>=n)
    {
        printf("yes");
    }
    else
    {
        printf("no");
    }
    getch();
}
