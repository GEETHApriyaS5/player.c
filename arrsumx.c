#include<stdio.h>
#include<conio.h>
void main()
{
    int n,x,a[100],i,j,c=0;
    scanf("%d",&n);
    scanf("%d",&x);
    for(i=0;i<n;i++)
    {
        scanf("%d",&a[i]);
    }
    for(i=0;i<n;i++)
    {
        for(j=i+1;j<n;j++)
        {
            if(a[i]+a[j]==x)
            {
                c++;
            }
        }
    }
    if(c>0)
    {
        printf("yes");
    }
    else
    {
        printf("no");
    }
    getch();
}
